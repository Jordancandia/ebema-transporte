// Normaliza un nombre de comuna para buscarlo en COORDS_COMUNAS.
// Convierte a minúsculas y elimina tildes.
function normalizarComuna(nombre) {
  return String(nombre || '')
    .toLowerCase()
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '') // elimina diacríticos
    .replace(/\s+/g, ' ')
    .trim();
}

/**
 * Busca coordenadas de una comuna en la tabla estática local.
 * @param {string} comuna - Nombre de la comuna
 * @returns {{ lat: number, lon: number } | null} - Coordenadas o null si no encontrada
 */
export function getCoordsComuna(comuna) {
  const key = normalizarComuna(comuna);
  if (!key) return null;
  return COORDS_COMUNAS[key] || null;
}
